#!/usr/bin/env python
"""Post-process vectorized rc_human mode evaluations.

This script combines per-mode outputs generated by
eval_rc_human_curriculum_levels_parallel.py and saves aggregate plots plus one
detailed curve figure for every evaluated episode.
"""
import argparse
from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd


def parse_mode_level(raw):
    if ":" not in raw:
        raise argparse.ArgumentTypeError("mode level must be formatted as MODE:LEVEL")
    mode, level = raw.split(":", 1)
    return int(mode), int(level)


def parse_args():
    p = argparse.ArgumentParser()
    p.add_argument("--run-dir", required=True)
    p.add_argument("--episode", type=int, required=True)
    p.add_argument(
        "--mode-level",
        action="append",
        type=parse_mode_level,
        required=True,
        help="Mode and level pair, e.g. 0:19. Can be repeated.",
    )
    p.add_argument("--output-dir", required=True)
    p.add_argument("--source-template", default="render_episode_{episode}_mode{mode}_level{level}_{episodes}_gpu_parallel")
    p.add_argument("--episodes-per-mode", type=int, default=50)
    p.add_argument("--max-per-episode-plots", type=int, default=0)
    return p.parse_args()


def read_inputs(args):
    run_dir = Path(args.run_dir)
    summaries = []
    traces = []
    for mode, level in args.mode_level:
        src = run_dir / args.source_template.format(
            episode=args.episode,
            mode=mode,
            level=level,
            episodes=args.episodes_per_mode,
        )
        summary_path = src / "rc_human_curriculum_level_summary_raw.csv"
        trace_path = src / "rc_human_curriculum_level_traces.csv"
        if not summary_path.exists() or not trace_path.exists():
            raise FileNotFoundError(f"Missing eval files under {src}")
        summary = pd.read_csv(summary_path)
        trace = pd.read_csv(trace_path)
        summary["eval_mode"] = mode
        trace["eval_mode"] = mode
        summary["source_dir"] = str(src)
        trace["source_dir"] = str(src)
        summaries.append(summary)
        traces.append(trace)
    return pd.concat(summaries, ignore_index=True), pd.concat(traces, ignore_index=True)


def add_derived_columns(summary, trace):
    trace["abs_yaw_deg"] = np.degrees(np.abs(trace["yaw_err"]))
    trace["yaw_err_deg"] = np.degrees(trace["yaw_err"])
    trace["att_deg"] = np.degrees(trace["att_err"])
    trace["roll_deg"] = np.degrees(trace["roll"])
    trace["pitch_deg"] = np.degrees(trace["pitch"])
    trace["vx_abs_err"] = np.abs(trace["local_vx"] - trace["target_vx"])
    trace["vy_abs_err"] = np.abs(trace["local_vy"] - trace["target_vy"])
    trace["vz_abs_err"] = np.abs(trace["vz"] - trace["target_vz"])
    summary["is_bad_done"] = summary["term_type"] == "bad_done"


def save_episode_metrics(summary, out_dir):
    fig, axes = plt.subplots(2, 3, figsize=(15, 8))
    plots = [
        ("length", "Episode length", "steps"),
        ("return", "Return", "return"),
        ("vel_mae", "Velocity MAE", "m/s"),
        ("yaw_mae_deg", "Yaw MAE", "deg"),
        ("att_mae_deg", "Attitude MAE", "deg"),
        ("action_delta_mean", "Action delta", "mean |a_t-a_{t-1}|"),
    ]
    modes = sorted(summary["eval_mode"].unique())
    labels = [f"mode{mode}" for mode in modes]
    for ax, (key, title, ylabel) in zip(axes.ravel(), plots):
        data = [summary[summary.eval_mode == mode][key].to_numpy() for mode in modes]
        ax.boxplot(data, labels=labels, showmeans=True)
        ax.set_title(title)
        ax.set_ylabel(ylabel)
        ax.grid(alpha=0.3)
    fig.suptitle("RC human policy mode evaluation")
    fig.tight_layout()
    fig.savefig(out_dir / "mode_eval_episode_metrics.png", dpi=150)
    plt.close(fig)


def save_tracking_envelope(summary, trace, out_dir):
    modes = sorted(trace["eval_mode"].unique())
    metrics = [
        ("vel_err", "Velocity error", "m/s", 0.75, "vx min limit"),
        ("abs_yaw_deg", "Abs yaw error", "deg", 20.0, "bad_done yaw"),
        ("att_deg", "Attitude error", "deg", 25.0, "pitch limit"),
    ]
    fig, axes = plt.subplots(len(modes), 4, figsize=(18, 3.2 * len(modes)), squeeze=False)
    for row, mode in enumerate(modes):
        mt = trace[trace.eval_mode == mode]
        for col, (key, title, ylabel, limit, limit_label) in enumerate(metrics):
            q = mt.groupby("step")[key].quantile([0.1, 0.5, 0.9]).unstack()
            times = q.index.to_numpy() * 0.02
            ax = axes[row, col]
            ax.plot(times, q[0.5], lw=1.5, label="median")
            ax.fill_between(times, q[0.1], q[0.9], alpha=0.25, label="10-90%")
            ax.axhline(limit, color="r", ls="--", lw=1, alpha=0.7, label=limit_label)
            ax.set_title(f"mode {mode}: {title}")
            ax.set_ylabel(ylabel)
            ax.grid(alpha=0.3)
            if row == 0 and col == 0:
                ax.legend(fontsize=7)
        ax = axes[row, 3]
        active_count = mt.groupby("step")["episode"].nunique()
        ax.plot(active_count.index.to_numpy() * 0.02, active_count.to_numpy(), lw=1.5)
        ax.set_ylim(0, max(1, summary[summary.eval_mode == mode].shape[0] + 2))
        ax.set_title(f"mode {mode}: active episodes")
        ax.set_ylabel("count")
        ax.grid(alpha=0.3)
    for ax in axes[-1, :]:
        ax.set_xlabel("time (s)")
    fig.suptitle("Tracking envelopes by mode")
    fig.tight_layout()
    fig.savefig(out_dir / "mode_eval_tracking_envelope.png", dpi=150)
    plt.close(fig)


def final_frame_candidates(summary, trace):
    final = (
        trace.sort_values(["eval_mode", "episode", "step"])
        .groupby(["eval_mode", "episode"], as_index=False)
        .tail(1)
    )
    final = final.merge(
        summary[
            [
                "eval_mode",
                "episode",
                "level",
                "mode_id",
                "length",
                "term_type",
                "success",
                "return",
                "vel_mae",
                "yaw_mae_deg",
                "att_mae_deg",
            ]
        ],
        on=["eval_mode", "episode"],
        how="left",
        suffixes=("", "_ep"),
    )
    final["vx_limit_final"] = np.maximum(0.75, np.abs(final["target_vx"]) * 0.25)
    final["velocity_tracking_active_final"] = final["command_transient_left"] <= 0
    final["candidate_tracking_velocity"] = final["velocity_tracking_active_final"] & (
        (final["vx_abs_err"] > final["vx_limit_final"])
        | (final["vy_abs_err"] > 0.5)
        | (final["vz_abs_err"] > 0.5)
    )
    final["candidate_tracking_yaw"] = final["abs_yaw_deg"] > 20.0
    final["candidate_tracking_any"] = (
        final["candidate_tracking_velocity"] | final["candidate_tracking_yaw"]
    )
    final["candidate_extreme_angle"] = (
        (np.abs(final["roll_deg"]) > 30.0) | (np.abs(final["pitch_deg"]) > 25.0)
    )
    final["body_rate_abs_max_final"] = final[["p", "q", "r"]].abs().max(axis=1)
    final["candidate_body_rate_proxy"] = final["body_rate_abs_max_final"] > 4.0

    def primary(row):
        if row["term_type"] != "bad_done":
            return "done"
        if row["candidate_extreme_angle"]:
            return "extreme_angle_candidate"
        if row["candidate_tracking_yaw"]:
            return "tracking_yaw_candidate"
        if row["candidate_tracking_velocity"]:
            if row["vx_abs_err"] > row["vx_limit_final"]:
                return "tracking_vx_candidate"
            if row["vy_abs_err"] > 0.5:
                return "tracking_vy_candidate"
            if row["vz_abs_err"] > 0.5:
                return "tracking_vz_candidate"
            return "tracking_velocity_candidate"
        if row["candidate_body_rate_proxy"]:
            return "body_rate_proxy_candidate"
        return "unknown_or_unlogged"

    final["primary_bad_done_candidate"] = final.apply(primary, axis=1)
    return final


def save_bad_done_plots(final, out_dir):
    bad = final[final["term_type"] == "bad_done"]
    if bad.empty:
        return
    counts = (
        bad.groupby(["eval_mode", "primary_bad_done_candidate"])
        .size()
        .unstack(fill_value=0)
    )
    fig, ax = plt.subplots(figsize=(11, 5))
    counts.plot(kind="bar", stacked=True, ax=ax)
    ax.set_xlabel("mode")
    ax.set_ylabel("bad_done count")
    ax.set_title("Primary bad_done candidate from final frame")
    ax.grid(axis="y", alpha=0.3)
    fig.tight_layout()
    fig.savefig(out_dir / "mode_eval_bad_done_candidates.png", dpi=150)
    plt.close(fig)


def save_overlay_plots(trace, out_dir):
    overlay_dir = out_dir / "overlay_plots"
    overlay_dir.mkdir(parents=True, exist_ok=True)
    for mode in sorted(trace["eval_mode"].unique()):
        mt = trace[trace.eval_mode == mode]
        fig, axes = plt.subplots(3, 2, figsize=(15, 11), sharex=True)
        axes = axes.ravel()
        for (_mode, episode), ep in mt.groupby(["eval_mode", "episode"]):
            t = ep["time_s"].to_numpy()
            alpha = 0.18
            axes[0].plot(t, ep["local_vx"], color="C0", alpha=alpha)
            axes[0].plot(t, ep["target_vx"], color="C1", alpha=alpha)
            axes[1].plot(t, ep["local_vy"], color="C0", alpha=alpha)
            axes[1].plot(t, ep["target_vy"], color="C1", alpha=alpha)
            axes[1].plot(t, ep["vz"], color="C2", alpha=alpha)
            axes[1].plot(t, ep["target_vz"], color="C3", alpha=alpha)
            axes[2].plot(t, ep["vel_err"], color="C0", alpha=alpha)
            axes[3].plot(t, ep["yaw_err_deg"], color="C0", alpha=alpha)
            axes[4].plot(t, ep["att_deg"], color="C0", alpha=alpha)
            axes[5].plot(t, ep["wind_north"], color="C0", alpha=alpha)
            axes[5].plot(t, ep["wind_east"], color="C1", alpha=alpha)
            axes[5].plot(t, ep["wind_down"], color="C2", alpha=alpha)
        titles = [
            "vx and target vx",
            "vy/vz and targets",
            "velocity error",
            "yaw error deg",
            "attitude error deg",
            "wind N/E/D",
        ]
        ylabels = ["m/s", "m/s", "m/s", "deg", "deg", "m/s"]
        for ax, title, ylabel in zip(axes, titles, ylabels):
            ax.set_title(title)
            ax.set_ylabel(ylabel)
            ax.grid(alpha=0.3)
        axes[2].axhline(0.75, color="r", ls="--", lw=1)
        axes[3].axhline(20.0, color="r", ls="--", lw=1)
        axes[3].axhline(-20.0, color="r", ls="--", lw=1)
        axes[4].axhline(25.0, color="r", ls="--", lw=1)
        for ax in axes[-2:]:
            ax.set_xlabel("time (s)")
        fig.suptitle(f"mode {mode}: all episode curves")
        fig.tight_layout()
        fig.savefig(overlay_dir / f"mode{mode}_all_episodes_overlay.png", dpi=150)
        plt.close(fig)


def plot_episode(ep, out_png):
    t = ep["time_s"].to_numpy()
    mode = int(ep["eval_mode"].iloc[0])
    level = int(ep["level"].iloc[0])
    episode = int(ep["episode"].iloc[0])
    fig, axes = plt.subplots(5, 2, figsize=(17, 16), sharex=True)
    axes = axes.ravel()

    axes[0].plot(t, ep["target_vx"], label="target vx")
    axes[0].plot(t, ep["target_vy"], label="target vy")
    axes[0].plot(t, ep["target_vz"], label="target vz")
    axes[0].plot(t, ep["target_yaw_rate"], label="target yaw rate")
    axes[0].set_title("Command targets")

    axes[1].plot(t, ep["raw_vx"], label="raw vx")
    axes[1].plot(t, ep["raw_vy"], label="raw vy")
    axes[1].plot(t, ep["raw_vz"], label="raw vz")
    axes[1].plot(t, ep["raw_yaw"], label="raw yaw")
    axes[1].set_title("Raw sticks")

    axes[2].plot(t, ep["stick_vx"], label="stick vx")
    axes[2].plot(t, ep["stick_vy"], label="stick vy")
    axes[2].plot(t, ep["stick_vz"], label="stick vz")
    axes[2].plot(t, ep["stick_yaw"], label="stick yaw")
    axes[2].set_title("PX4 processed sticks")

    axes[3].plot(t, ep["local_vx"], label="local vx")
    axes[3].plot(t, ep["target_vx"], "--", label="target vx")
    axes[3].plot(t, ep["local_vy"], label="local vy")
    axes[3].plot(t, ep["target_vy"], "--", label="target vy")
    axes[3].set_title("Horizontal velocity tracking")

    axes[4].plot(t, ep["vz"], label="vz")
    axes[4].plot(t, ep["target_vz"], "--", label="target vz")
    axes[4].set_title("Vertical velocity tracking")

    axes[5].plot(t, ep["vel_err"], label="vel err")
    axes[5].axhline(0.75, color="r", ls="--", lw=1, label="vx min limit")
    axes[5].set_title("Velocity error")

    axes[6].plot(t, ep["yaw_err_deg"], label="yaw err deg")
    axes[6].axhline(20.0, color="r", ls="--", lw=1)
    axes[6].axhline(-20.0, color="r", ls="--", lw=1)
    axes[6].set_title("Yaw error")

    axes[7].plot(t, ep["roll_deg"], label="roll deg")
    axes[7].plot(t, ep["pitch_deg"], label="pitch deg")
    axes[7].plot(t, ep["att_deg"], label="att err deg")
    axes[7].axhline(25.0, color="r", ls="--", lw=1)
    axes[7].axhline(-25.0, color="r", ls="--", lw=1)
    axes[7].set_title("Attitude")

    axes[8].plot(t, ep["wind_north"], label="wind north")
    axes[8].plot(t, ep["wind_east"], label="wind east")
    axes[8].plot(t, ep["wind_down"], label="wind down")
    axes[8].set_title("Wind")

    axes[9].plot(t, ep["f1"], label="f1")
    axes[9].plot(t, ep["f2"], label="f2")
    axes[9].plot(t, ep["f3"], label="f3")
    axes[9].plot(t, ep["f4"], label="f4")
    axes[9].plot(t, ep["f5"], label="f5")
    axes[9].set_title("Actuator outputs")

    for ax in axes:
        ax.grid(alpha=0.3)
        ax.legend(fontsize=7, ncol=2)
    for ax in axes[-2:]:
        ax.set_xlabel("time (s)")
    fig.suptitle(f"mode {mode} level {level} episode {episode}")
    fig.tight_layout()
    fig.savefig(out_png, dpi=130)
    plt.close(fig)


def save_per_episode_plots(trace, out_dir, max_plots):
    ep_dir = out_dir / "per_episode_plots"
    ep_dir.mkdir(parents=True, exist_ok=True)
    plotted = 0
    for (mode, episode), ep in trace.groupby(["eval_mode", "episode"]):
        if max_plots > 0 and plotted >= max_plots:
            break
        plot_episode(ep, ep_dir / f"mode{int(mode)}_episode{int(episode):02d}.png")
        plotted += 1
    return plotted


def save_summary_text(summary, final, out_dir):
    rows = []
    for mode in sorted(summary["eval_mode"].unique()):
        s = summary[summary.eval_mode == mode]
        f = final[final.eval_mode == mode]
        bad = f[f.term_type == "bad_done"]
        rows.append(
            {
                "mode": mode,
                "episodes": len(s),
                "success_rate": float((s.term_type != "bad_done").mean()),
                "bad_done_count": int((s.term_type == "bad_done").sum()),
                "mean_length": float(s.length.mean()),
                "mean_return": float(s["return"].mean()),
                "vel_mae": float(s.vel_mae.mean()),
                "vel_rmse": float(s.vel_rmse.mean()),
                "yaw_mae_deg": float(s.yaw_mae_deg.mean()),
                "att_mae_deg": float(s.att_mae_deg.mean()),
                "candidate_tracking_any": int(bad.candidate_tracking_any.sum()),
                "candidate_tracking_yaw": int(bad.candidate_tracking_yaw.sum()),
                "candidate_tracking_velocity": int(bad.candidate_tracking_velocity.sum()),
                "candidate_extreme_angle": int(bad.candidate_extreme_angle.sum()),
                "candidate_body_rate_proxy": int(bad.candidate_body_rate_proxy.sum()),
                "unknown_or_unlogged": int(
                    (bad.primary_bad_done_candidate == "unknown_or_unlogged").sum()
                ),
            }
        )
    mode_summary = pd.DataFrame(rows)
    mode_summary.to_csv(out_dir / "mode_eval_summary.csv", index=False)
    counts = final[final.term_type == "bad_done"].groupby(
        ["eval_mode", "primary_bad_done_candidate"]
    ).size()
    lines = [
        "RC human mode evaluation summary.",
        "",
        mode_summary.to_string(index=False),
        "",
        "term_type_counts:",
        summary.groupby(["eval_mode", "term_type"]).size().to_string(),
        "",
        "primary_bad_done_candidate_counts:",
        counts.to_string() if len(counts) else "(none)",
        "",
        "Cause labels are inferred from the final recorded frame using current rc_human thresholds. The environment does not currently log exact termination-condition names.",
    ]
    (out_dir / "mode_eval_analysis_summary.txt").write_text("\n".join(lines), encoding="utf-8")


def main():
    args = parse_args()
    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    summary, trace = read_inputs(args)
    add_derived_columns(summary, trace)
    summary.to_csv(out_dir / "mode_eval_episode_summary.csv", index=False)
    trace.to_csv(out_dir / "mode_eval_traces.csv", index=False)

    save_episode_metrics(summary, out_dir)
    save_tracking_envelope(summary, trace, out_dir)
    save_overlay_plots(trace, out_dir)
    final = final_frame_candidates(summary, trace)
    final.to_csv(out_dir / "mode_eval_bad_done_candidates.csv", index=False)
    save_bad_done_plots(final, out_dir)
    plotted = save_per_episode_plots(trace, out_dir, args.max_per_episode_plots)
    save_summary_text(summary, final, out_dir)
    print(f"[saved] {out_dir}")
    print(f"[saved] per-episode plots: {plotted}")


if __name__ == "__main__":
    main()
